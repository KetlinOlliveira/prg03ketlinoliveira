    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try{ 
            Long id = Long.valueOf(txtID.getText());//Pega o ID informado na caixa
            Curso curso = cursoService.findById(id); //faz a buscar no ID usando o cursoService
            
            if(curso != null){//Imprime as informações do ID se encontrar
                String resultado = "CURSO ENCONTRADO:\n" + "ID:" + curso.getId()+
                        "| Nome: " + curso.getNome() + "\n" + "| QTD: " + curso.getQuantidade() +
                        "| Descrição: " + curso.getDescricao();
                
                JOptionPane.showMessageDialog(this, resultado, "Busca por ID", JOptionPane.INFORMATION_MESSAGE);
            }
        } catch (NumberFormatException e) {//Tratamento de exceções
        JOptionPane.showMessageDialog(this, "Por favor, insira um ID numérico válido.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
        JOptionPane.showMessageDialog(this, "Erro ao buscar curso: " + e.getMessage(), "Erro de Persistência", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

