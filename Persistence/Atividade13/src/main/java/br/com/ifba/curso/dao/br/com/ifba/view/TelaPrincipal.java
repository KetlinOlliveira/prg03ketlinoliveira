    // Responsável por buscar todos os cursos e exibir na tela.
    public void carregarCursos(){
        try{
            //Pede ao Service para buscar todos os cursos no banco de dados.
            List<Curso> cursos = cursoService.findAll(); 
            // Objeto usado para construir o texto que será exibido de forma eficiente.
            StringBuilder sb = new StringBuilder(); 
            
            //Checa se a lista de cursos veio vazia.
            if(cursos.isEmpty()){
                sb.append("Nenhum curso cadastrado");
            }else{
                // 3. FORMATAÇÃO: Percorre a lista de cursos.
                for(Curso c: cursos){
                    // Constrói uma linha formatada (ID | Nome | Qtd) para cada curso.
                    sb.append("ID: ").append(c.getId())
                    .append(" | Nome: ").append(c.getNome() != null ? c.getNome() : "N/A") // Tratamento simples se o Nome for nulo.
                    .append(" | Qtd: ").append(c.getQuantidade() != 0 ? c.getQuantidade() : "N/A") // Tratamento simples se a Qtd for zero.
                    .append("\n"); // Adiciona uma quebra de linha.
                }
            }
            //Coloca o texto formatado (sb) na área de texto da tela (txtListar).
            txtListar.setText(sb.toString()); 
            
        }catch (Exception e) {
            //Bloco de tratamento de erro se houver falha na comunicação com o Service/Banco.
            txtListar.setText("Erro ao carregar cursos: " + e.getMessage());
            JOptionPane.showMessageDialog(this, "Erro ao carregar cursos: " + e.getMessage(), "Erro de Persistência", JOptionPane.ERROR_MESSAGE);
        }
    }
