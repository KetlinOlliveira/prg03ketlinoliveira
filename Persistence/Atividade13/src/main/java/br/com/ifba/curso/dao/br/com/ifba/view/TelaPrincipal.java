    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try{//Ação para excluir um cadastro
            Long id = Long.parseLong(txtID.getText().trim());//Recebe ID
                
            Curso cursoParaExcluir = cursoDao.findById(id); //faz a busca pelo ID e transfere para cursoParaExcluir
                
            
                if(cursoParaExcluir != null){
                    int confirmacao = JOptionPane.showConfirmDialog(this, "Certeza que deseja excluir o curso ID: " + id + "?",
                        "Confirmar Exclusão", JOptionPane.YES_NO_OPTION);//faz a confirmação com o usuario
                    
                if(confirmacao == JOptionPane.YES_OPTION){
                    cursoDao.delete(cursoParaExcluir);
                    JOptionPane.showMessageDialog(this, "Curso com ID " + id + "Excluido com sucesso!");
                    carregarCursos();
                    txtID.setText("");                 
                }

            }else{
                    JOptionPane.showMessageDialog(this, "Curso com id: "+ id + "não encontrado");
                }      
                //Tratamento de exceçoes
        }catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Por favor, digite um ID numérico válido.", "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Erro ao excluir: " + e.getMessage(), "Erro de Banco de Dados", JOptionPane.ERROR_MESSAGE);
        }


        // TODO add your handling code here:
    }//GEN-LAST:event_jButton2ActionPerformed

