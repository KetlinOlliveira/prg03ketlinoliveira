    public TelaPrincipal(ListarCursos telapai, Curso curso){
        initComponents();
        this.telapai = telapai;
        this.setTitle("Editar Curso");
        
        if(curso != null){
            try{
                txtnome.setText(curso.getNome());
                txtDescricao.setText(curso.getDescricao());
                txtFornecedor.setText(curso.getFornecedor());
                txtQuant.setText(String.valueOf(curso.getQuantidade()));
   
            }catch(Exception e){
                JOptionPane.showMessageDialog(this, "Erro ao carregar o curso" + e.getMessage(),
                "Erro", JOptionPane.ERROR_MESSAGE);
            }
        }else{
                JOptionPane.showMessageDialog(this, "O curso selecionado não foi encontrado ou está vazio",
                "Aviso", JOptionPane.WARNING_MESSAGE);
        }
        
    }

