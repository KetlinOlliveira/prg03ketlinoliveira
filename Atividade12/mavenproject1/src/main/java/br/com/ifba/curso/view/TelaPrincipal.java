    private void jToggleButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jToggleButton1ActionPerformed

        try{
            
            String nome = txtnome.getText();//recebe todos os campos digitados
            int quantidade = Integer.parseInt(txtQuant.getText());
            String descricao = txtDescricao.getText();
            String fornecedor = txtFornecedor.getText();
            
            Curso novoCurso = new Curso(1L,nome, quantidade, descricao, fornecedor); //cria um novo curso
            //salva o obj e imprime uma mensagem
            cursoDAO.salvar(novoCurso);
            
            JOptionPane.showMessageDialog(this, "Curso:" + nome+ " cadastrado com sucesso!", "Sucesso" , JOptionPane.INFORMATION_MESSAGE);
        }catch( NumberFormatException e){//faz as exceções
            JOptionPane.showMessageDialog(this, 
            "Erro: A quantidade deve ser um número válido.", 
            "Erro de Entrada", JOptionPane.ERROR_MESSAGE);
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, 
            "Erro ao salvar: Verifique a conexão com o banco.", 
            "Erro de Persistência", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace();
        }





        // TODO add your handling code here:
    }//GEN-LAST:event_jToggleButton1ActionPerformed

